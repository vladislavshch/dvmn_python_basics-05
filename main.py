fake = Faker("ru_RU")


def main():
    os.makedirs("Characters", exist_ok=True)
    for i in range(10):
        skill = random.sample(skills, 3)
        runic_skill = ""
        runic_skills = []

        for value in skill:
            for letter in value:
                letter = letter.replace(letter, runic_alphabet[letter])
                runic_skill += letter
            runic_skills.append(runic_skill)
            runic_skill = ""

        character = {
            "first_name": fake.first_name(),
            "last_name": fake.last_name(),
            "job": fake.job(),
            "town": fake.city(),
            "strength": random.randint(3, 18),
            "agility": random.randint(3, 18),
            "endurance": random.randint(3, 18),
            "intelligence": random.randint(3, 18),
            "luck": random.randint(3, 18),
            "skill_1": runic_skills[0],
            "skill_2": runic_skills[1],
            "skill_3": runic_skills[2]
        }

        i += 1
        file_name = "character_" + str(i)
        file_operations.render_template("charsheet.svg", f"Characters/{file_name}.svg", character)


if __name__ == '__main__':
    main()
